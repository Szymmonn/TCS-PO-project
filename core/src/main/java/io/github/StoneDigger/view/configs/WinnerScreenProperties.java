package io.github.StoneDigger.view.configs;

public class WinnerScreenProperties {
    public final float worldWidth;
    public final float worldHeight;
    public final int wonFontSize;
    public final String wonFontBorderColor;
    public final String wonFontColor;
    public final float wonLabelWidth;
    public final float wonLabelHeight;
    public final float wonLabelPositionX;
    public final float wonLabelPositionY;
    public final int backFontSize;
    public final String backFontBorderColor;
    public final String backFontColor;
    public final float backLabelWidth;
    public final float backLabelHeight;
    public final float backLabelPositionX;
    public final float backLabelPositionY;


    public WinnerScreenProperties(float worldWidth,
                                 float worldHeight,
                                 int wonFontSize,
                                 String wonFontBorderColor,
                                 String wonFontColor,
                                 float wonLabelWidth,
                                 float wonLabelHeight,
                                 float wonLabelPositionX,
                                 float wonLabelPositionY,
                                 int backFontSize,
                                 String backFontBorderColor,
                                 String backFontColor,
                                 float backLabelWidth,
                                 float backLabelHeight,
                                 float backLabelPositionX,
                                 float backLabelPositionY) {
        this.worldWidth = worldWidth;
        this.worldHeight = worldHeight;
        this.wonFontSize = wonFontSize;
        this.wonFontBorderColor = wonFontBorderColor;
        this.wonFontColor = wonFontColor;
        this.wonLabelWidth = wonLabelWidth;
        this.wonLabelHeight = wonLabelHeight;
        this.wonLabelPositionX = wonLabelPositionX;
        this.wonLabelPositionY = wonLabelPositionY;
        this.backFontSize = backFontSize;
        this.backFontBorderColor = backFontBorderColor;
        this.backFontColor = backFontColor;
        this.backLabelWidth = backLabelWidth;
        this.backLabelHeight = backLabelHeight;
        this.backLabelPositionX = backLabelPositionX;
        this.backLabelPositionY = backLabelPositionY;
    }
}
